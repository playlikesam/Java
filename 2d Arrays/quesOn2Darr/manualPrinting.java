package quesOn2Darr;
public class manualPrinting{
    public static void main(String[] args){
        //initialization and declaration :
        int[][] arr = new int[2][2];

        arr[0][0] = 1;
        arr[0][1] = 2;

        arr[1][0] = 3;
        arr[1][1] = 4;

        //Printing :
        System.out.println(arr[0][0]+" "+arr[0][1]+" "+arr[1][0]+" "+arr[1][1]);
    }
}
