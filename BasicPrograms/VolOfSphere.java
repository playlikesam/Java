package BasicPrograms;
public class VolOfSphere {
    public static void main(String[] args) {
        //Homework : Calculate Volume of Sphere.
        // Here Vol. of Sphere is 4/3*pi*r*r*r

        double r = 3.6;
        double v = (4*3.14*r*r*r)/3;
        System.out.println("Volume of Sphere with Radius"+r+" is :"+v);
    }
}
