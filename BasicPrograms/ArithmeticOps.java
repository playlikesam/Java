package BasicPrograms;
public class ArithmeticOps {
    public static void main(String[] args){
        int x,y;
        x = 4;
        y = 2;

//        Addition
        System.out.println(x+y);
//        subtraction
        System.out.println(x-y);
//        multiplication
        System.out.println(x*y);
//        Division
        System.out.println(x/y);
    }
}
